package com.ciss.wuliu.entity;

public class TurnPage {
	
	private int pageNo;
	private int resnum;
	private int allres;
	private int allpage;
	
	public int getPageNo() {
		return pageNo;
	}
	public void setPageNo(int pageNo) {
		this.pageNo = pageNo;
	}
	public int getResnum() {
		return resnum;
	}
	public void setResnum(int resnum) {
		this.resnum = resnum;
	}
	public int getAllres() {
		return allres;
	}
	public void setAllres(int allres) {
		this.allres = allres;
	}
	public int getAllpage() {
		return allpage;
	}
	public void setAllpage(int allpage) {
		this.allpage = allpage;
	}
}
